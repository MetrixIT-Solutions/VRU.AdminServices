const SetRes = require('../SetRes');

const postMailServerListVldn = (req) => {
  const reqBody = req.body;
  if (!req.headers.vruadatoken) {
    const te = SetRes.tokenRequired();
    return { flag: false, result: te };
  } else {
    return { flag: true };
  }
}

const postMailServerCrtVldn = (req) => {
  const reqBody = req.body;
  if (!req.headers.vruadatoken) {
    const te = SetRes.tokenRequired();
    return { flag: false, result: te };
  } else if (!reqBody.fromMail || !reqBody.fromMailPswd || !reqBody.mailServerHost || !reqBody.mailServerPort || !reqBody.senderAdrs) {
    const ad = SetRes.msdReqFields();
    return { flag: false, result: ad };
  } else {
    return { flag: true };
  }
}

module.exports = {
  postMailServerListVldn, postMailServerCrtVldn
};
