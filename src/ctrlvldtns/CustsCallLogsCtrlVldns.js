const SetRes = require('../SetRes');

const postCustsCallLogCreate = (req) => {
  const reqBody = req.body;
  if (!req.headers.vruadatoken) {
    const te = SetRes.tokenRequired();
    return { flag: false, result: te };
  } else if (!reqBody.customerData ||  !reqBody.customerData.mobNum || !reqBody.customerData.mobCcNum || !reqBody.customerData.mobCc || !reqBody.callStatus) {
    const ad = SetRes.msdReqFields();
    return { flag: false, result: ad };
  } else {
    return { flag: true };
  }
}

module.exports = {
  postCustsCallLogCreate
}
